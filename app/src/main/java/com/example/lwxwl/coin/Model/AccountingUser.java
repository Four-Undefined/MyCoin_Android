package com.example.lwxwl.coin.Model;


public class AccountingUser {

    public int edu;
    public int trip;
    public int diet;
    public int normal;
    public int clothes;
    public int enter;
    public int month;
    public int day;

    public AccountingUser(int edu, int trip, int diet, int normal, int clothes, int enter, int month, int day) {
        this.edu = edu;
        this.trip = trip;
        this.diet = diet;
        this.normal = normal;
        this.clothes = clothes;
        this.enter = enter;
        this.month = month;
        this.day = day;
    }
}
